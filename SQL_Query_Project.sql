-- Q.11 Write a SQL query to find the best selling category by total revenue in each month.



SELECT *
FROM 
(
SELECT 
	DATE_FORMAT(sale_date, '%Y-%m') AS month,
    category,
    SUM(total_sale) AS total_revenue,
    RANK() OVER(PARTITION BY DATE_FORMAT(sale_date, '%Y-%m')
    ORDER BY SUM(total_sale) DESC) AS ranking
FROM retail_sales
GROUP BY DATE_FORMAT(sale_date, '%Y-%m'), category
)AS monthly_sales
WHERE ranking =1;


-- Q.12 Write a SQL query to find customers who have made more than 3 transactions.


SELECT customer_id, COUNT(transactions_id) AS total_count
FROM retail_sales
GROUP BY customer_id
HAVING COUNT(transactions_id) > 3;


-- Q.13 Write a SQL query to find the percentage contribution of each category to the total revenue.


SELECT category,
SUM(total_sale) AS sum_total,
SUM(total_sale)/ (SELECT SUM(total_sale) FROM retail_sales) * 100 AS Percentage
FROM retail_sales
GROUP BY category;


-- Q.14 Write a SQL query to retrieve the most recent transaction for each customer.


