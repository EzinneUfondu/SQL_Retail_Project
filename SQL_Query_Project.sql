-- Q.15 Write a SQL query to find customers who have purchased from more than one category.

SELECT customer_id,
	COUNT(DISTINCT category)
     FROM retail_sales
     GROUP BY customer_id
     HAVING COUNT(DISTINCT category) > 1;


 -- Q.16 Write a SQL query to find the month with the highest number of transactions for each category.


SELECT * FROM (
    SELECT 
        DATE_FORMAT(sale_date, '%Y-%m') AS month,
        category,
        COUNT(transactions_id) AS total_transactions,
        RANK() OVER(PARTITION BY category ORDER BY COUNT(transactions_id) DESC) AS ranking
    FROM retail_sales
    GROUP BY DATE_FORMAT(sale_date, '%Y-%m'), category
) AS highest_transactions
WHERE ranking = 1;

-- Q.17 Write a SQL query to rank customers based on their total spending using a window function.

SELECT customer_id,
    SUM(total_sale) AS total_spending,
    RANK() OVER(ORDER BY SUM(total_sale) DESC) AS ranking
FROM retail_sales
GROUP BY customer_id;


-- Q.18 Write a SQL query to calculate a running total of sales ordered by date.

SELECT sale_date,
    SUM(total_sale) AS daily_sale,
    SUM(SUM(total_sale)) OVER(ORDER BY sale_date) AS running_total
FROM retail_sales
GROUP BY sale_date;


-- Q.19 Write a SQL query to find the average transaction value per age group (e.g. 18-25, 26-35, 36-50, 51+)

SELECT 
    CASE
        WHEN age BETWEEN 18 AND 25 THEN '18-25'
        WHEN age BETWEEN 26 AND 35 THEN '26-35'
        WHEN age BETWEEN 36 AND 50 THEN '36-50'
        ELSE '51+'
    END AS age_group,
    ROUND(AVG(total_sale), 2) AS avg_transaction
FROM retail_sales
GROUP BY age_group;


-- Q.20 Write a SQL query to identify customers who haven't made a purchase in the last 3 months of the dataset.       

SELECT DISTINCT customer_id
FROM retail_sales
WHERE customer_id NOT IN (
    SELECT DISTINCT customer_id
    FROM retail_sales
    WHERE sale_date >= DATE_SUB((SELECT MAX(sale_date) FROM retail_sales), INTERVAL 3 MONTH)
);


